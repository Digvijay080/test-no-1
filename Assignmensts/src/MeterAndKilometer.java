import java.util.Scanner;
public class MeterAndKilometer {

	public static void main(String[] args) {
	  double c,m,k;
	  Scanner sc = new Scanner(System.in);
	  System.out.println("Enter length into centimeter : ");
	  c=sc.nextDouble();
	  
	  m = c/100;
	  System.out.println("Meter : "+m+ "m ");
	  
	  k=c/100000;
	  System.out.println("Kilometer : "+k+"km");


	
	
	
	
	
	
	
	
	
	}

}
