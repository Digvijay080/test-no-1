package inheritance.parentandchild;

public class Fruit {
	void test()
	{
		System.out.println("Fruits are generally sweet to teste");
	}
}
  class Apple extends Fruit
  {
	  void test()
	{
		System.out.println("Apples are sweet to teste");
	}
  }
  class Strawberry extends Fruit
  {
	  void test()
		{
			System.out.println("Strawberries are sweet and sour in teste");
		}
  }

